var (
	mockCoverageRepo = &core.Repository{
		ID:        1,
		Namespace: "octocat",
		Name:      "hello-world",
		Branch:    "master",
	}

	mockCoverageBuildGood = &core.Build{
		ID:       1,
		RepoID:   1,
		Number:   1,
		Status:   core.StatusPassing,
		Ref:      "refs/heads/develop",
		Coverage: 100.00,
	}

	mockCoverageBuildMedium = &core.Build{
		ID:       2,
		RepoID:   1,
		Number:   2,
		Status:   core.StatusFailing,
		Ref:      "refs/heads/master",
		Coverage: 84.00,
	}

	mockCoverageBuildBad = &core.Build{
		ID:       3,
		RepoID:   1,
		Number:   3,
		Status:   core.StatusRunning,
		Ref:      "refs/heads/master",
		Coverage: 74.00,
	}
)

func TestHandlerCoverage(t *testing.T) {
	controller := gomock.NewController(t)
	defer controller.Finish()

	repos := mock.NewMockRepositoryStore(controller)
	repos.EXPECT().FindName(gomock.Any(), gomock.Any(), mockCoverageRepo.Name).Return(mockCoverageRepo, nil)

	builds := mock.NewMockBuildStore(controller)
	builds.EXPECT().FindRef(gomock.Any(), mockCoverageRepo.ID, "refs/heads/develop").Return(mockCoverageBuildGood, nil)

	c := new(chi.Context)
	c.URLParams.Add("owner", "octocat")
	c.URLParams.Add("name", "hello-world")

	w := httptest.NewRecorder()
	r := httptest.NewRequest("GET", "/?ref=refs/heads/develop", nil)
	r = r.WithContext(
		context.WithValue(context.Background(), chi.RouteCtxKey, c),
	)

	HandlerCoverage(repos, builds)(w, r)
	if got, want := w.Code, 200; want != got {
		t.Errorf("Want response code %d, got %d", want, got)
	}
	if got, want := w.Header().Get("Access-Control-Allow-Origin"), "*"; got != want {
		t.Errorf("Want Access-Control-Allow-Origin %q, got %q", want, got)
	}
	if got, want := w.Header().Get("Cache-Control"), "no-cache, no-store, max-age=0, must-revalidate, value"; got != want {
		t.Errorf("Want Cache-Control %q, got %q", want, got)
	}
	if got, want := w.Header().Get("Content-Type"), "image/svg+xml"; got != want {
		t.Errorf("Want Access-Control-Allow-Origin %q, got %q", want, got)
	}
	if got, want := w.Body.String(), fmt.Sprintf(badgeCoverageGoog, mockCoverageBuildGood.Coverage); got != want {
		t.Errorf("Want badge %q, got %q", got, want)
	}
}

